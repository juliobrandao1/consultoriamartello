     <h2>Finanças e Cidadania</h2>
	<p>
	
A Consultoria Martello surgiu através de uma trajetória que envolve aprendizado e paixão pelo tema de finanças e pela ideia de ajudar as pessoas. Após anos de experiência de seu idealizador Thiago Martello no mundo financeiro, a empresa foi tomando corpo e contando com a ajuda de colaboradores no decorrer do caminho, sempre com o princípio de levar à sociedade conhecimento e ajuda sobre como lidar com dinheiro. Percebeu-se que o grau de expansão sobre Educação Financeira no Brasil é baixíssimo, e isso é fato preocupante não só para os indivíduos afetados, mas para economia nacional como um todo.
 
Hoje, a empresa oferece consultorias pessoais e também palestras gratuitas em escolas públicas, universidades, ONGs e empresas, pois acreditamos que levar informação sobre finanças é tão importante quanto ensinar sobre outras áreas do conhecimento, independente da classe social e condição financeira.
Contamos com um diferencial para uma maior abrangência de pessoas: a tradução e interpretação do conteúdo por intérprete de LIBRAS – Língua Brasileira de Sinais, para deficientes auditivos, que infelizmente não possuem muitos canais de inserção na sociedade.
 
Nosso objetivo é simplificar a vida das pessoas a quem prestamos serviços, através de linguagem simples e jovial, sem deixar de lado jamais a seriedade, honestidade e acima de tudo, amor pelo que fazemos.
	</p>

